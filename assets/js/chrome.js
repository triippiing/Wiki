/* ──────────────────────────────────────────────────────────────
   chrome.js — runtime page furniture for stone-theme runbooks.

   Builds three things at load, so a runbook adopts them with zero
   edits to its own markup:

     1. .code-panel  — wraps every .cmd in a header bar (language
        label left, ⧉ COPY right), moving any existing inline copy
        button into it.
     2. step accordions — binds .step-header click handlers.
     3. .rail        — the 212px category sidebar, built from
        window.WIKI_NAV (assets/js/nav-data.js, generated by
        scripts/build_index.py). No-ops if nav-data.js is absent.

   Language label: set data-lang on the .cmd to override ("shell").
   ────────────────────────────────────────────────────────────── */

(function () {
  'use strict';

  /* ── site root ─────────────────────────────────────────────
     Runbooks live at depth 1 (vtl/x.html) or depth 2
     (aix/administration/x.html) and link assets with a matching
     number of ../ hops. Recover the root from this script's own
     src — everything before "assets/" — so nav hrefs resolve from
     either depth. data-root on the <script> tag overrides.        */
  function siteRoot() {
    var me = document.currentScript ||
             document.querySelector('script[src$="assets/js/chrome.js"]');
    if (me && me.dataset.root) return me.dataset.root;
    if (me) {
      var i = me.getAttribute('src').lastIndexOf('assets/');
      if (i !== -1) return me.getAttribute('src').slice(0, i);
    }
    return '';
  }

  function copyText(cmd, btn) {
    navigator.clipboard.writeText(cmd.innerText.trim()).then(function () {
      btn.textContent = '⧉ COPIED';
      btn.classList.add('copied');
      setTimeout(function () {
        btn.textContent = '⧉ COPY';
        btn.classList.remove('copied');
      }, 1800);
    });
  }

  function buildPanels() {
    document.querySelectorAll('.cmd').forEach(function (cmd) {
      if (cmd.closest('.code-panel')) return;

      var panel = document.createElement('div');
      panel.className = 'code-panel';
      cmd.parentNode.insertBefore(panel, cmd);

      var head = document.createElement('div');
      head.className = 'code-head';

      var lang = document.createElement('span');
      lang.className = 'code-lang';
      lang.textContent = cmd.dataset.lang || 'shell';
      head.appendChild(lang);

      /* Reuse the button already in the markup if there is one, so the
         old onclick="copy(id,this)" pages keep a working button either way. */
      var btn = cmd.querySelector('.copy-btn');
      if (btn) {
        btn.removeAttribute('onclick');
      } else {
        btn = document.createElement('button');
        btn.className = 'copy-btn';
      }
      btn.type = 'button';
      btn.textContent = '⧉ COPY';
      btn.addEventListener('click', function () { copyText(cmd, btn); });
      head.appendChild(btn);

      panel.appendChild(head);
      panel.appendChild(cmd);
    });
  }

  function bindSteps() {
    document.querySelectorAll('.step-header').forEach(function (h) {
      h.removeAttribute('onclick');
      h.addEventListener('click', function () {
        h.parentElement.classList.toggle('open');
      });
    });
  }

  /* ── last-reviewed ────────────────────────────────────────
     Appends a "Last reviewed" cell to the .meta grid from the page's
     <meta name="reviewed" content="YYYY-MM-DD">. Deliberately NOT
     derived from git: a cosmetic commit that touches every runbook
     would otherwise reset every date and make a stale runbook look
     fresh, which is the exact failure this field exists to prevent.
     A missing date is reported loudly, not hidden.                  */

  var MONTH = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
               'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

  function monthsSince(d) {
    var now = new Date();
    return (now.getFullYear() - d.getFullYear()) * 12 +
           (now.getMonth() - d.getMonth()) -
           (now.getDate() < d.getDate() ? 1 : 0);
  }

  function ageLabel(months) {
    if (months < 1)  return 'this month';
    if (months < 24) return months + (months === 1 ? ' month' : ' months');
    var years = Math.floor(months / 12);
    return years + ' years';
  }

  function buildReviewed() {
    var grid = document.querySelector('.meta');
    if (!grid || grid.querySelector('.meta-reviewed')) return;

    var tag = document.querySelector('meta[name="reviewed"]');
    var raw = tag && tag.getAttribute('content');

    var cell = document.createElement('div');
    cell.className = 'meta-cell meta-reviewed';

    var label = document.createElement('div');
    label.className = 'meta-label';
    label.textContent = 'Last reviewed';

    var val = document.createElement('div');
    val.className = 'meta-val';

    var badge = document.createElement('span');
    badge.className = 'meta-age';

    var d = raw ? new Date(raw + 'T00:00:00') : null;
    if (!d || isNaN(d)) {
      val.classList.add('never');
      val.textContent = '—';
      badge.classList.add('stale');
      badge.textContent = 'never reviewed';
    } else {
      var months = monthsSince(d);
      val.textContent = String(d.getDate()).padStart(2, '0') + ' ' +
                        MONTH[d.getMonth()] + ' ' + d.getFullYear();
      badge.classList.add(months < 6 ? 'fresh' : months < 12 ? 'ageing' : 'stale');
      badge.textContent = ageLabel(months);
    }

    val.appendChild(badge);
    cell.appendChild(label);
    cell.appendChild(val);
    grid.appendChild(cell);
  }


  /* ── sidebar ─────────────────────────────────────────────── */

  /* Match the current page against a nav href. Filenames contain spaces
     ("VTL tomcat patching and hardening.html") and nav-data stores them
     percent-encoded, so compare on the decoded tail of the path. */
  function isCurrent(href) {
    var here = decodeURIComponent(location.pathname);
    var there = decodeURIComponent(href);
    return here.endsWith('/' + there) || here === '/' + there;
  }

  function buildRail() {
    if (!Array.isArray(window.WIKI_NAV) || document.querySelector('.rail')) return;

    var root = siteRoot();

    var nav = document.createElement('nav');
    nav.className = 'rail';
    nav.setAttribute('aria-label', 'Runbooks');

    var mark = document.createElement('a');
    mark.className = 'rail-mark';
    mark.href = root + 'index.html';
    mark.textContent = 'Knowledge Base';
    nav.appendChild(mark);

    /* Filter box. The whole nav tree is already in memory via WIKI_NAV, so
       search works from inside any runbook — no trip back to the index. */
    var search = document.createElement('div');
    search.className = 'rail-search';
    var input = document.createElement('input');
    input.className = 'rail-input';
    input.type = 'search';
    input.placeholder = 'Filter runbooks  /';
    input.setAttribute('aria-label', 'Filter runbooks');
    search.appendChild(input);
    nav.appendChild(search);

    var empty = document.createElement('div');
    empty.className = 'rail-empty';
    empty.textContent = 'no matches';

    window.WIKI_NAV.forEach(function (cat) {
      var group = document.createElement('div');
      group.className = 'rail-group';

      var label = document.createElement('div');
      label.className = 'rail-cat';
      label.textContent = cat.name;
      group.appendChild(label);

      cat.pages.forEach(function (page) {
        var a = document.createElement('a');
        a.className = 'rail-link';
        a.href = root + page.href;
        a.textContent = page.title;
        /* Match on category and sub-folder too, so "lvm" or "tsm" finds the
           right pages even when the words aren't in the title. */
        a.dataset.search = (page.title + ' ' + cat.name + ' ' + (page.sub || '')).toLowerCase();
        if (isCurrent(page.href)) a.setAttribute('aria-current', 'page');
        group.appendChild(a);
      });

      nav.appendChild(group);
    });

    nav.appendChild(empty);

    function applyFilter() {
      var q = input.value.trim().toLowerCase();
      var hits = 0;

      nav.querySelectorAll('.rail-group').forEach(function (group) {
        var shown = 0;
        group.querySelectorAll('.rail-link').forEach(function (a) {
          var match = !q || a.dataset.search.indexOf(q) !== -1;
          a.classList.toggle('hide', !match);
          if (match) shown++;
        });
        group.classList.toggle('hide', shown === 0);
        hits += shown;
      });

      empty.classList.toggle('show', hits === 0);
    }

    input.addEventListener('input', applyFilter);

    var toggle = document.createElement('button');
    toggle.className = 'rail-toggle';
    toggle.type = 'button';
    toggle.setAttribute('aria-label', 'Toggle navigation');
    toggle.setAttribute('aria-expanded', 'false');
    toggle.setAttribute('aria-controls', 'rail');
    toggle.appendChild(document.createElement('span'));
    nav.id = 'rail';

    var scrim = document.createElement('div');
    scrim.className = 'rail-scrim';

    function setOpen(open) {
      document.body.classList.toggle('rail-open', open);
      toggle.setAttribute('aria-expanded', String(open));
    }

    toggle.addEventListener('click', function () {
      setOpen(!document.body.classList.contains('rail-open'));
    });
    scrim.addEventListener('click', function () { setOpen(false); });

    document.addEventListener('keydown', function (e) {
      /* "/" focuses the filter — but not while the user is typing into
         something, or it would swallow slashes in a path. */
      var typing = /^(INPUT|TEXTAREA|SELECT)$/.test(e.target.tagName) ||
                   e.target.isContentEditable;
      if (e.key === '/' && !typing && !e.metaKey && !e.ctrlKey) {
        e.preventDefault();
        setOpen(true);
        input.focus();
        return;
      }

      if (e.key === 'Escape') {
        /* Escape clears a live filter first; only close the drawer once
           there is nothing left to clear. */
        if (input.value) {
          input.value = '';
          applyFilter();
          return;
        }
        input.blur();
        setOpen(false);
      }
    });

    document.body.appendChild(toggle);
    document.body.appendChild(scrim);
    document.body.appendChild(nav);

    /* Keep the active entry in view when the rail is taller than the
       viewport — on a 23-page nav the current page is often below the fold.
       Set scrollTop directly rather than calling scrollIntoView, which walks
       every scrollable ancestor and would also scroll the document away from
       the top of the runbook on load. */
    var active = nav.querySelector('.rail-link[aria-current="page"]');
    if (active && nav.scrollHeight > nav.clientHeight) {
      nav.scrollTop = active.offsetTop - (nav.clientHeight / 2);
    }
  }

  function init() {
    buildPanels();
    bindSteps();
    buildReviewed();
    buildRail();
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
