/* ──────────────────────────────────────────────────────────────
   chrome.js — runtime page furniture for stone-theme runbooks.

   Builds three things at load, so a runbook adopts them with zero
   edits to its own markup:

     1. .code-panel  — wraps every .cmd in a header bar (language
        label left, copy-icon button right), moving any existing inline copy
        button into it.
     2. step accordions — binds .step-header click handlers.
     3. .rail        — the 212px category sidebar, built from
        window.WIKI_NAV (assets/js/nav-data.js, generated by
        scripts/build_index.py). No-ops if nav-data.js is absent.

   Language label: set data-lang on the .cmd to override ("shell").
   ────────────────────────────────────────────────────────────── */

(function () {
  'use strict';

  /* ── preferences store ─────────────────────────────────────
     localStorage throws in private windows / when disabled, so every
     access is guarded — a broken store just means prefs don't persist,
     never a broken page. */
  var THEME_KEY = 'wiki-theme';       /* "light" | "dark"              */
  var RAIL_KEY  = 'wiki-rail';        /* "collapsed" | "open"          */

  function getPref(key) {
    try { return localStorage.getItem(key); } catch (e) { return null; }
  }
  function setPref(key, val) {
    try { localStorage.setItem(key, val); } catch (e) { /* ignore */ }
  }

  /* ── theme, applied as early as this script runs ───────────
     A saved choice wins; otherwise fall back to the OS preference. We
     always write an explicit data-theme so the icon swap and the rest of
     the CSS have one thing to test. tokens.css also carries a
     prefers-color-scheme fallback for the (rare) case this never runs. */
  function resolveTheme() {
    var saved = getPref(THEME_KEY);
    if (saved === 'dark' || saved === 'light') return saved;
    return window.matchMedia &&
           window.matchMedia('(prefers-color-scheme: dark)').matches
             ? 'dark' : 'light';
  }
  document.documentElement.setAttribute('data-theme', resolveTheme());

  /* ── icons (from the wiki style spec — Feather-weight, 1.75 stroke) ── */
  var ICON = {
    home:    '<svg viewBox="0 0 24 24"><path d="M3 11l9-7 9 7"></path><path d="M5 10v9a1 1 0 0 0 1 1h4v-6h4v6h4a1 1 0 0 0 1-1v-9"></path></svg>',
    sidebar: '<svg viewBox="0 0 24 24"><rect x="3" y="4" width="18" height="16" rx="2"></rect><line x1="9" y1="4" x2="9" y2="20"></line></svg>',
    moon:    '<svg class="i-moon" viewBox="0 0 24 24"><path d="M20 13A8 8 0 1 1 11 4a6.2 6.2 0 0 0 9 9z"></path></svg>',
    sun:     '<svg class="i-sun" viewBox="0 0 24 24"><circle cx="12" cy="12" r="4"></circle><path d="M12 2v2M12 20v2M4.9 4.9l1.4 1.4M17.7 17.7l1.4 1.4M2 12h2M20 12h2M4.9 19.1l1.4-1.4M17.7 6.3l1.4-1.4"></path></svg>',
    copy:    '<svg viewBox="0 0 24 24"><rect x="9" y="9" width="11" height="11" rx="2"></rect><path d="M5 15V5a2 2 0 0 1 2-2h8"></path></svg>',
    check:   '<svg viewBox="0 0 24 24"><path d="M20 6L9 17l-5-5"></path></svg>'
  };

  /* copy-button contents: the spec's copy glyph (a check on success) + label */
  function copyBtnHTML(label) {
    return (label === 'COPIED' ? ICON.check : ICON.copy) +
           '<span class="copy-label">' + label + '</span>';
  }

  /* ── site root ─────────────────────────────────────────────
     Runbooks live at depth 1 (vtl/x.html) or depth 2
     (aix/administration/x.html) and link assets with a matching
     number of ../ hops. Recover the root from this script's own
     src — everything before "assets/" — so nav hrefs resolve from
     either depth. data-root on the <script> tag overrides.        */
  function siteRoot() {
    var me = document.currentScript ||
             document.querySelector('script[src$="assets/js/chrome.js"]');
    if (me && me.dataset.root) return me.dataset.root;
    if (me) {
      var i = me.getAttribute('src').lastIndexOf('assets/');
      if (i !== -1) return me.getAttribute('src').slice(0, i);
    }
    return '';
  }

  function copyText(cmd, btn) {
    navigator.clipboard.writeText(cmd.innerText.trim()).then(function () {
      btn.innerHTML = copyBtnHTML('COPIED');
      btn.classList.add('copied');
      setTimeout(function () {
        btn.innerHTML = copyBtnHTML('COPY');
        btn.classList.remove('copied');
      }, 1800);
    });
  }

  function buildPanels() {
    document.querySelectorAll('.cmd').forEach(function (cmd) {
      if (cmd.closest('.code-panel')) return;

      var panel = document.createElement('div');
      panel.className = 'code-panel';
      cmd.parentNode.insertBefore(panel, cmd);

      var head = document.createElement('div');
      head.className = 'code-head';

      var lang = document.createElement('span');
      lang.className = 'code-lang';
      lang.textContent = cmd.dataset.lang || 'shell';
      head.appendChild(lang);

      /* Reuse the button already in the markup if there is one, so the
         old onclick="copy(id,this)" pages keep a working button either way. */
      var btn = cmd.querySelector('.copy-btn');
      if (btn) {
        btn.removeAttribute('onclick');
      } else {
        btn = document.createElement('button');
        btn.className = 'copy-btn';
      }
      btn.type = 'button';
      btn.innerHTML = copyBtnHTML('COPY');
      btn.addEventListener('click', function () { copyText(cmd, btn); });
      head.appendChild(btn);

      panel.appendChild(head);
      panel.appendChild(cmd);
    });
  }

  /* ── steps ────────────────────────────────────────────────
     Binds the accordions and gives every step a stable anchor so a runbook
     can be handed over mid-incident ("start at step 6") as a link that
     lands on the step and opens it.

     The id comes from the .step-num badge ("01", "1.1", "RB.4"), not the
     step's position, so inserting a step above another doesn't silently
     repoint every link that was already shared.                          */

  function stepId(text) {
    return 'step-' + text.trim().toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/^-+|-+$/g, '');
  }

  function bindSteps() {
    var used = {};

    document.querySelectorAll('.step').forEach(function (step, i) {
      var header = step.querySelector('.step-header');
      if (!header) return;

      header.removeAttribute('onclick');
      header.addEventListener('click', function () {
        step.classList.toggle('open');
      });

      if (step.id) return;

      var num = step.querySelector('.step-num');
      var id = (num && num.textContent.trim()) ? stepId(num.textContent)
                                               : 'step-' + (i + 1);
      /* A runbook can legitimately repeat a badge across phases; keep the
         first as-is and suffix the rest so ids stay unique. */
      used[id] = (used[id] || 0) + 1;
      if (used[id] > 1) id += '-' + used[id];
      step.id = id;

      var link = document.createElement('a');
      link.className = 'step-link';
      link.href = '#' + id;
      link.textContent = '#';
      link.title = 'Copy link to this step';
      link.setAttribute('aria-label', 'Copy link to this step');

      link.addEventListener('click', function (e) {
        e.preventDefault();
        e.stopPropagation();          /* don't toggle the accordion */
        history.replaceState(null, '', '#' + id);
        step.classList.add('open');
        navigator.clipboard.writeText(location.href).then(function () {
          link.textContent = '✓';
          link.classList.add('copied');
          setTimeout(function () {
            link.textContent = '#';
            link.classList.remove('copied');
          }, 1400);
        });
      });

      var chevron = header.querySelector('.step-chevron');
      if (chevron) header.insertBefore(link, chevron);
      else header.appendChild(link);
    });
  }

  /* Open and reveal the step named in the URL. */
  function openFromHash() {
    var id = decodeURIComponent(location.hash.slice(1));
    if (!id) return;

    var step = document.getElementById(id);
    if (!step || !step.classList.contains('step')) return;

    step.classList.add('open');

    var reduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
    step.scrollIntoView({ block: 'start', behavior: reduced ? 'auto' : 'smooth' });

    step.classList.add('flash');
    setTimeout(function () { step.classList.remove('flash'); }, 1800);
  }

  /* ── last-reviewed ────────────────────────────────────────
     Appends a "Last reviewed" cell to the .meta grid from the page's
     <meta name="reviewed" content="YYYY-MM-DD">. Deliberately NOT
     derived from git: a cosmetic commit that touches every runbook
     would otherwise reset every date and make a stale runbook look
     fresh, which is the exact failure this field exists to prevent.
     A missing date is reported loudly, not hidden.                  */

  var MONTH = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
               'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

  function monthsSince(d) {
    var now = new Date();
    return (now.getFullYear() - d.getFullYear()) * 12 +
           (now.getMonth() - d.getMonth()) -
           (now.getDate() < d.getDate() ? 1 : 0);
  }

  function ageLabel(months) {
    if (months < 1)  return 'this month';
    if (months < 24) return months + (months === 1 ? ' month' : ' months');
    var years = Math.floor(months / 12);
    return years + ' years';
  }

  function buildReviewed() {
    var grid = document.querySelector('.meta');
    if (!grid || grid.querySelector('.meta-reviewed')) return;

    var tag = document.querySelector('meta[name="reviewed"]');
    var raw = tag && tag.getAttribute('content');

    var cell = document.createElement('div');
    cell.className = 'meta-cell meta-reviewed';

    var label = document.createElement('div');
    label.className = 'meta-label';
    label.textContent = 'Last reviewed';

    var val = document.createElement('div');
    val.className = 'meta-val';

    var badge = document.createElement('span');
    badge.className = 'meta-age';

    var d = raw ? new Date(raw + 'T00:00:00') : null;
    if (!d || isNaN(d)) {
      val.classList.add('never');
      val.textContent = '—';
      badge.classList.add('stale');
      badge.textContent = 'never reviewed';
    } else {
      var months = monthsSince(d);
      val.textContent = String(d.getDate()).padStart(2, '0') + ' ' +
                        MONTH[d.getMonth()] + ' ' + d.getFullYear();
      badge.classList.add(months < 6 ? 'fresh' : months < 12 ? 'ageing' : 'stale');
      badge.textContent = ageLabel(months);
    }

    val.appendChild(badge);
    cell.appendChild(label);
    cell.appendChild(val);
    grid.appendChild(cell);
  }


  /* ── sidebar ─────────────────────────────────────────────── */

  /* Match the current page against a nav href. Filenames contain spaces
     ("VTL tomcat patching and hardening.html") and nav-data stores them
     percent-encoded, so compare on the decoded tail of the path. */
  function isCurrent(href) {
    var here = decodeURIComponent(location.pathname);
    var there = decodeURIComponent(href);
    return here.endsWith('/' + there) || here === '/' + there;
  }

  /* ── rail toggle, shared by the pill (trigger) and buildRail (drawer) ──
     Below 1080px the rail is an overlay drawer (body.rail-open); at desktop
     widths it is a persistent column that collapses (body.rail-collapsed).
     One button in the control pill drives whichever model is in play. */
  var railRefs = { btn: null, input: null };

  function railIsDrawer() {
    return window.matchMedia &&
           window.matchMedia('(max-width: 1079px)').matches;
  }

  function setDrawer(open) {
    document.body.classList.toggle('rail-open', open);
    if (railRefs.btn) railRefs.btn.setAttribute('aria-expanded', String(open));
  }

  function toggleRail() {
    if (railIsDrawer()) {
      setDrawer(!document.body.classList.contains('rail-open'));
    } else {
      var collapsed = document.body.classList.toggle('rail-collapsed');
      setPref(RAIL_KEY, collapsed ? 'collapsed' : 'open');
      if (railRefs.btn) railRefs.btn.setAttribute('aria-pressed', String(collapsed));
    }
  }

  /* Make the rail visible in whichever model, so "/" can then focus its filter. */
  function revealRail() {
    if (railIsDrawer()) {
      setDrawer(true);
    } else if (document.body.classList.contains('rail-collapsed')) {
      document.body.classList.remove('rail-collapsed');
      setPref(RAIL_KEY, 'open');
      if (railRefs.btn) railRefs.btn.setAttribute('aria-pressed', 'false');
    }
  }

  function buildRail() {
    if (!Array.isArray(window.WIKI_NAV) || document.querySelector('.rail')) return;

    var root = siteRoot();

    var nav = document.createElement('nav');
    nav.className = 'rail';
    nav.setAttribute('aria-label', 'Runbooks');

    /* Filter box. The whole nav tree is already in memory via WIKI_NAV, so
       search works from inside any runbook — no trip back to the index.
       (The old "Knowledge Base" wordmark link lived here; its job — going
       home — is now the pill's home button.) */
    var search = document.createElement('div');
    search.className = 'rail-search';
    var input = document.createElement('input');
    input.className = 'rail-input';
    input.type = 'search';
    input.placeholder = 'filter runbooks';
    input.setAttribute('aria-label', 'Filter runbooks');
    search.appendChild(input);
    nav.appendChild(search);
    railRefs.input = input;

    var empty = document.createElement('div');
    empty.className = 'rail-empty';
    empty.textContent = 'no matches';

    window.WIKI_NAV.forEach(function (cat) {
      var group = document.createElement('div');
      group.className = 'rail-group';

      var label = document.createElement('div');
      label.className = 'rail-cat';
      label.textContent = cat.name;
      group.appendChild(label);

      cat.pages.forEach(function (page) {
        var a = document.createElement('a');
        a.className = 'rail-link';
        a.href = root + page.href;
        a.textContent = page.title;
        /* Match on category and sub-folder too, so "lvm" or "tsm" finds the
           right pages even when the words aren't in the title. */
        a.dataset.search = (page.title + ' ' + cat.name + ' ' + (page.sub || '')).toLowerCase();
        if (isCurrent(page.href)) a.setAttribute('aria-current', 'page');
        group.appendChild(a);
      });

      nav.appendChild(group);
    });

    nav.appendChild(empty);

    function applyFilter() {
      var q = input.value.trim().toLowerCase();
      var hits = 0;

      nav.querySelectorAll('.rail-group').forEach(function (group) {
        var shown = 0;
        group.querySelectorAll('.rail-link').forEach(function (a) {
          var match = !q || a.dataset.search.indexOf(q) !== -1;
          a.classList.toggle('hide', !match);
          if (match) shown++;
        });
        group.classList.toggle('hide', shown === 0);
        hits += shown;
      });

      empty.classList.toggle('show', hits === 0);
    }

    input.addEventListener('input', applyFilter);

    nav.id = 'rail';

    /* Scrim behind the drawer on narrow screens; tapping it closes the rail. */
    var scrim = document.createElement('div');
    scrim.className = 'rail-scrim';
    scrim.addEventListener('click', function () { setDrawer(false); });

    document.addEventListener('keydown', function (e) {
      /* "/" reveals the rail and focuses the filter — but not while the user is
         typing into something, or it would swallow slashes in a path. */
      var typing = /^(INPUT|TEXTAREA|SELECT)$/.test(e.target.tagName) ||
                   e.target.isContentEditable;
      if (e.key === '/' && !typing && !e.metaKey && !e.ctrlKey) {
        e.preventDefault();
        revealRail();
        input.focus();
        return;
      }

      if (e.key === 'Escape') {
        /* Escape clears a live filter first; only close the drawer once
           there is nothing left to clear. */
        if (input.value) {
          input.value = '';
          applyFilter();
          return;
        }
        input.blur();
        setDrawer(false);
      }
    });

    document.body.appendChild(scrim);
    document.body.appendChild(nav);

    /* Keep the active entry in view when the rail is taller than the
       viewport — on a 23-page nav the current page is often below the fold.
       Set scrollTop directly rather than calling scrollIntoView, which walks
       every scrollable ancestor and would also scroll the document away from
       the top of the runbook on load. */
    var active = nav.querySelector('.rail-link[aria-current="page"]');
    if (active && nav.scrollHeight > nav.clientHeight) {
      nav.scrollTop = active.offsetTop - (nav.clientHeight / 2);
    }
  }

  /* ── control pill ─────────────────────────────────────────
     Home · sidebar · theme, pinned top-left where the old "Knowledge Base"
     wordmark used to sit. This single cluster replaces both that wordmark
     and the old narrow-screen hamburger — the sidebar button opens the drawer
     on narrow screens and collapses the column on desktop (see toggleRail). */
  function buildControls() {
    if (document.querySelector('.wiki-controls')) return;

    var pill = document.createElement('div');
    pill.className = 'wiki-controls';

    /* Home — back to the index (what the wordmark link used to do). A real
       <a> so middle-click / open-in-new-tab behave as expected. */
    var homeBtn = document.createElement('a');
    homeBtn.className = 'wiki-btn';
    homeBtn.href = siteRoot() + 'index.html';
    homeBtn.setAttribute('aria-label', 'Back to the knowledge base index');
    homeBtn.title = 'Home — knowledge base index';
    homeBtn.innerHTML = ICON.home;

    var railBtn = document.createElement('button');
    railBtn.type = 'button';
    railBtn.className = 'wiki-btn';
    railBtn.setAttribute('aria-label', 'Show or hide the sidebar');
    railBtn.setAttribute('aria-controls', 'rail');
    railBtn.title = 'Show / hide sidebar';
    railBtn.innerHTML = ICON.sidebar;
    railBtn.setAttribute('aria-pressed',
      String(document.body.classList.contains('rail-collapsed')));
    railBtn.addEventListener('click', toggleRail);
    railRefs.btn = railBtn;

    var themeBtn = document.createElement('button');
    themeBtn.type = 'button';
    themeBtn.className = 'wiki-btn';
    themeBtn.setAttribute('aria-label', 'Toggle light or dark theme');
    themeBtn.title = 'Toggle light / dark';
    themeBtn.innerHTML = ICON.moon + ICON.sun;
    themeBtn.addEventListener('click', function () {
      var next = document.documentElement.getAttribute('data-theme') === 'dark'
        ? 'light' : 'dark';
      document.documentElement.setAttribute('data-theme', next);
      setPref(THEME_KEY, next);
    });

    pill.appendChild(homeBtn);
    pill.appendChild(railBtn);
    pill.appendChild(themeBtn);
    document.body.appendChild(pill);
  }

  /* Restore a collapsed rail before first paint, with the transition briefly
     suppressed so a remembered-collapsed rail doesn't slide shut on load. */
  function applyRailState() {
    document.body.classList.add('rail-no-anim');
    if (getPref(RAIL_KEY) === 'collapsed') {
      document.body.classList.add('rail-collapsed');
    }
    requestAnimationFrame(function () {
      requestAnimationFrame(function () {
        document.body.classList.remove('rail-no-anim');
      });
    });
  }

  function init() {
    applyRailState();
    buildPanels();
    bindSteps();
    buildReviewed();
    buildRail();
    buildControls();
    openFromHash();
    window.addEventListener('hashchange', openFromHash);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
